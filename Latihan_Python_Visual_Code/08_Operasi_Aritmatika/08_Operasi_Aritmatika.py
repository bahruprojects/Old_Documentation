a = 10
b = 3

# Operasi Penjumlahan
hasil = a + b
print(a, '+', b,'=', hasil)


# Operasi Pengurangan
hasil = a - b
print(a, '-', b,'=', hasil)


# Operasi Perkalian
hasil = a * b
print(a, '8', b,'=', hasil)


# Operasi Pembagian
hasil = a / b
print(a, '/', b,'=', hasil)


# Operasi Exponensial (Pangkat)
hasil = a ** b
print(a, '**', b,'=', hasil)


# Operasi Modulus (Sisa Pembagian)
hasil = a % b
print(a, '%', b,'=', hasil)


# Operasi Floor Division (Kebalikan Modulus)
hasil = a // b
print(a, '//', b,'=', hasil)

# Prioritas Operasi

x = 3
y = 2
z = 4

hasil = x ** y * z + x / y - y % z // x
print(x, '**', y, '*', z, '+', x, '/', y, '-', y, '%', z, '//', x, '=', hasil)

hasil = x + y * z
print(x, '+', y, '*', z, '=', hasil)

hasil = (x + y) * z
print('(', x, '+', y, ')', '*', z, '=', hasil)








