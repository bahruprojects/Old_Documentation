# Pengenalan String


data = 'ini adalah string'
print(data)
print(type(data))

# Cara Membuat String Menggunakan " " dan ' '

data1 = 'Menggunakan Single Quote'
print(data1)

data2 = "Menggunakan Double Quote"
print(data2)

print(' "Halo" ')
print(" 'Halo' ")
print("Ini adalah hari Jum'at")

# Membuat Tanda \
print('hari jum\'at')
print('g\day, isn\'t it?')

# Backslash
print("C:\\user\\alfin")


# Tab
print("Alfin\t\t\t\t\ Bahru")

#Backspace
print("Alfin \Bahru")

# Newline
print("Baris1\nBaris2") # Line Feed
print("Baris1\rBaris2") # Carriage Return
print("Baris1\r\nBaris2") # Line Feed Carriage Return 


# String Literal

# Menggunakan Raw String (r)
print(r"C:\user\alfin")

# Multiline
print(""" 
nama : Alfin
kelas : 3
      """
)

#Multiline Literal String Dan RAW
print(r"""
Nama : Alfin
Website : www.Alfin.com/newID
"""
)






















