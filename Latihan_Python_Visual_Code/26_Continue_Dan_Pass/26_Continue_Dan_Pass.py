# Continue, Pass, Break


angka = 0
while angka < 10:
    angka +=1
    
    print(angka)

# Pass = Berfungsi sebagai dummy, tidak akan dieksekusi


angka = 0
while angka < 10:
    angka +=1
    
    if angka == 7:
        pass # Ini tidak akan dieksekusi

    print(angka)



# Continue

angka = 0
while angka < 10:
    angka +=1
    
    if angka == 7:
        continue

    print(angka)



# Break

angka = 0
while angka < 10:
    angka +=1
    
    if angka == 7:
        break

    print(angka)























































