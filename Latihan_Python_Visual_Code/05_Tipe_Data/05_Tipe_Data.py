# a = 10, a adalah variabel dengan nilai 10

# tipe data : Angka satuan(integer) yang nggak ada komanya
data_integer = 1000
print("Data : ", data_integer)
print('-- Data : ', type(data_integer))

# Tipe Data: Angka Dengan Koma (float)
data_float = 1.5
print("Data : ", data_float)
print('-- Data : ', type(data_float))

# Tipe Data: Angka Dengan Karakter (String)
data_string = "Alfin"
print("Data : ", data_string)
print('-- Data : ', type(data_string))

# Tipe Data: Angka Dengan Biner True/False (Boolean)
data_boolean = "Alfin"
print("Data : ", data_boolean)
print('-- Data : ', type(data_boolean))

## Tipe Data Khusus

# Bilangan Kompleks
data_complex = complex(5, 6)
print("Data : ", data_complex)
print('-- Data : ', type(data_complex))


# Tipe Data Dari Bahasa C

from ctypes import c_double

data_c_double = c_double(10.5)
print("Data : ", data_c_double )
print('-- Data : ', type(data_c_double))





